#!/usr/bin/env python
"""Evaluate one or more world-model checkpoints on a fixed test set
generated by ``examples/scripts/collect_wm_testset.py``.

For each trajectory we run the same autoregressive replay-LPIPS scoring
that the live reward server uses (``examples/reward_model/score_traj.py``),
then aggregate metrics overall, by success vs failure, and by the noise
scale that produced each trajectory (read from ``manifest.json``).

Output:
    <output>/per_traj.jsonl       one record per trajectory
    <output>/aggregate.json       summary metrics + per-bucket breakdowns
    <output>/predictions/<eid>/   gt_vs_pred.mp4 side-by-side (when --save-predictions)

Run from the open-world venv on a single GPU::

    cd /n/fs/iromdata/project/open-world
    OPEN_WORLD_ROOT=/n/fs/iromdata/project/open-world \\
    CUDA_VISIBLE_DEVICES=0 \\
    .venv/bin/python /n/fs/iromdata/project/dsrl_pi0/examples/scripts/eval_wm_on_testset.py \\
        --testset-dir /n/fs/.../wm_testsets/libero90_task57_v1 \\
        --ckpt-path /n/fs/.../wm_checkpoints/checkpoint-200.pt \\
        --output /n/fs/.../wm_eval/ckpt-200_on_libero90_task57_v1 \\
        --num-windows 4 --num-inference-steps 25 --save-predictions
"""
from __future__ import annotations

import argparse
import json
import os
import sys
import time
import traceback
from pathlib import Path

import numpy as np
import torch


OPEN_WORLD_ROOT = Path(
    os.environ.get("OPEN_WORLD_ROOT", "/n/fs/iromdata/project/open-world")
)

# Reuse the helpers the live reward server uses, so eval and live scoring
# can never diverge.
_REWARD_MODEL_DIR = Path(__file__).resolve().parent.parent / "reward_model"
sys.path.insert(0, str(_REWARD_MODEL_DIR))

from score_traj import (  # noqa: E402
    _load_libero_args,
    build_model,
    score_episode,
)
from reward_server import (  # noqa: E402
    _load_actions,
    _load_or_encode_latents,
    _read_annotation,
    _save_wm_predictions,
)


def _load_manifest(testset_dir: Path) -> dict:
    p = testset_dir / "manifest.json"
    if not p.exists():
        raise FileNotFoundError(
            f"{p} not found. Was this test set generated by "
            f"examples/scripts/collect_wm_testset.py?")
    with p.open() as f:
        return json.load(f)


def _list_eids(testset_dir: Path, manifest: dict) -> list[dict]:
    """Return the manifest entries actually present on disk (annotation +
    raw videos exist), in stable eid order."""
    out = []
    for entry in manifest.get("entries", []):
        eid = entry["eid"]
        # The reward server looks under both 'train' and 'val'.
        ann = None
        for split in ("train", "val"):
            cand = testset_dir / "annotation" / split / f"{eid}.json"
            if cand.exists():
                ann = cand
                break
        if ann is None:
            print(f"[eval] WARN: no annotation for eid={eid}, skipping")
            continue
        out.append(entry)
    out.sort(key=lambda e: e["eid"])
    return out


def _summarize(per_traj_records: list[dict]) -> dict:
    """Aggregate metrics overall + by is_success + by noise_scale.

    Per-traj record format::
        {eid, noise_scale, is_success, mean_lpips, T_wm,
         windows_completed, elapsed_s}
    """
    if not per_traj_records:
        return {"num_trajs": 0}

    def _stats(vals):
        a = np.asarray(vals, dtype=np.float64)
        return {
            "n": int(a.size),
            "mean": float(a.mean()),
            "std": float(a.std(ddof=0)),
            "min": float(a.min()),
            "max": float(a.max()),
            "median": float(np.median(a)),
        }

    overall = _stats([r["mean_lpips"] for r in per_traj_records])
    by_succ = {
        "success": _stats(
            [r["mean_lpips"] for r in per_traj_records if r["is_success"]]
        ) if any(r["is_success"] for r in per_traj_records) else None,
        "failure": _stats(
            [r["mean_lpips"] for r in per_traj_records if not r["is_success"]]
        ) if any(not r["is_success"] for r in per_traj_records) else None,
    }
    by_sigma: dict[str, dict] = {}
    sigmas = sorted({float(r["noise_scale"]) for r in per_traj_records})
    for s in sigmas:
        recs = [r for r in per_traj_records if float(r["noise_scale"]) == s]
        succ = [r for r in recs if r["is_success"]]
        fail = [r for r in recs if not r["is_success"]]
        by_sigma[f"{s:g}"] = {
            "all": _stats([r["mean_lpips"] for r in recs]),
            "success_rate": float(len(succ) / max(1, len(recs))),
            "n_success": len(succ),
            "n_failure": len(fail),
            "success_lpips": _stats([r["mean_lpips"] for r in succ]) if succ else None,
            "failure_lpips": _stats([r["mean_lpips"] for r in fail]) if fail else None,
        }

    return {
        "num_trajs": len(per_traj_records),
        "overall_lpips": overall,
        "by_success": by_succ,
        "by_noise_scale": by_sigma,
    }


def main():
    parser = argparse.ArgumentParser(
        description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument("--testset-dir", required=True,
                        help="Directory produced by collect_wm_testset.py")
    parser.add_argument("--ckpt-path", required=True,
                        help="WM checkpoint to evaluate.")
    parser.add_argument("--output", required=True,
                        help="Output directory for per_traj.jsonl + "
                             "aggregate.json (+ optional predictions/).")
    parser.add_argument("--config",
                        default=str(OPEN_WORLD_ROOT / "configs/training/libero_wm.py"))
    parser.add_argument("--dataset-root",
                        default=str(OPEN_WORLD_ROOT / "data/libero_processed"),
                        help="Where stat.json lives. Used to normalize "
                             "actions identically to training.")
    parser.add_argument("--num-windows", type=int, default=4)
    parser.add_argument("--start-frame", type=int, default=6)
    parser.add_argument("--skip-his", type=int, default=4)
    parser.add_argument("--num-inference-steps", type=int, default=25)
    parser.add_argument("--device", default="cuda:0")
    parser.add_argument("--save-predictions", action="store_true",
                        help="Save decoded WM predictions vs GT mp4s "
                             "under <output>/predictions/<eid>/.")
    parser.add_argument("--max-trajs", type=int, default=-1,
                        help="Cap eval at this many trajectories "
                             "(-1 = all entries in the manifest).")
    args = parser.parse_args()

    testset_dir = Path(args.testset_dir).resolve()
    output_dir = Path(args.output).resolve()
    output_dir.mkdir(parents=True, exist_ok=True)

    print(f"[eval] testset:  {testset_dir}")
    print(f"[eval] ckpt:     {args.ckpt_path}")
    print(f"[eval] output:   {output_dir}")

    manifest = _load_manifest(testset_dir)
    entries = _list_eids(testset_dir, manifest)
    if args.max_trajs > 0:
        entries = entries[: args.max_trajs]
    print(f"[eval] {len(entries)} trajectories to score")

    # --- model ---
    cfg = _load_libero_args(Path(args.config))
    if not os.path.isabs(cfg.svd_model_path):
        cfg.svd_model_path = str(OPEN_WORLD_ROOT / cfg.svd_model_path)
    if not os.path.isabs(cfg.clip_model_path):
        cfg.clip_model_path = str(OPEN_WORLD_ROOT / cfg.clip_model_path)
    if args.num_inference_steps:
        cfg.num_inference_steps = args.num_inference_steps
    print(f"[eval] cfg num_inference_steps={cfg.num_inference_steps}  "
          f"num_windows={args.num_windows}  start_frame={args.start_frame}")

    device = torch.device(args.device)
    model, pipeline, pipeline_cls = build_model(cfg, args.ckpt_path, device)

    print("[eval] loading LPIPS...")
    import lpips as lpips_mod
    lpips_fn = lpips_mod.LPIPS(net="alex", verbose=False).to(device).eval()

    # --- iterate ---
    per_traj_path = output_dir / "per_traj.jsonl"
    per_traj_records: list[dict] = []
    pred_root = output_dir / "predictions"
    if args.save_predictions:
        pred_root.mkdir(parents=True, exist_ok=True)

    with per_traj_path.open("w") as out_f:
        for k, entry in enumerate(entries):
            eid = entry["eid"]
            t0 = time.perf_counter()
            try:
                # Find which split holds this episode.
                split = "train"
                if not (testset_dir / "annotation" / split / f"{eid}.json").exists():
                    split = "val"
                ann = _read_annotation(
                    testset_dir / "annotation" / split / f"{eid}.json")

                latents_per_cam = _load_or_encode_latents(
                    traj_dir=testset_dir, annotation=ann,
                    cfg=cfg, pipeline=pipeline, device=device, eid=eid,
                )
                T_wm = latents_per_cam.shape[0]
                actions = _load_actions(
                    ann, cfg, testset_dir, Path(args.dataset_root), T_wm,
                )
                text = (ann["texts"][0] if ann.get("texts")
                        else ann.get("language_instruction", ""))

                result = score_episode(
                    model=model, pipeline=pipeline, pipeline_cls=pipeline_cls,
                    cfg=cfg,
                    latents_per_cam=latents_per_cam, actions=actions, text=text,
                    start_frame=args.start_frame,
                    num_windows=args.num_windows,
                    skip_his=args.skip_his,
                    lpips_fn=lpips_fn, device=device,
                    autoregressive=True, verbose=False,
                    return_rgb=args.save_predictions,
                )
                elapsed = time.perf_counter() - t0

                rec = {
                    "eid": eid,
                    "noise_scale": float(entry.get("noise_scale", float("nan"))),
                    "is_success": bool(entry.get("is_success", False)),
                    "env_steps": int(entry.get("env_steps", 0)),
                    "env_seed": int(entry.get("env_seed", -1)),
                    "T_wm": int(T_wm),
                    "windows_completed": int(result["windows_completed"]),
                    "mean_lpips": float(result["mean_lpips"]),
                    "per_frame_lpips": [float(x) for x in result["per_frame_lpips"]],
                    "elapsed_s": elapsed,
                    "ckpt_path": args.ckpt_path,
                }
                if args.save_predictions and "pred_rgb" in result:
                    try:
                        meta = _save_wm_predictions(
                            output_dir, eid,
                            result["pred_rgb"], result["gt_rgb"],
                            fps=int(getattr(cfg, "fps", 10)),
                        )
                        rec.update({"video_path": meta["video_path"]})
                    except Exception as e:
                        print(f"[eval] WARN: save predictions for {eid} failed: {e}")

                out_f.write(json.dumps(rec) + "\n")
                out_f.flush()
                per_traj_records.append(rec)

                print(
                    f"[eval] [{k+1:3d}/{len(entries)}] eid={eid}  "
                    f"sigma={rec['noise_scale']}  succ={rec['is_success']}  "
                    f"mean_lpips={rec['mean_lpips']:.4f}  "
                    f"windows={rec['windows_completed']}  T_wm={T_wm}  "
                    f"elapsed={elapsed:.1f}s"
                )
            except Exception:
                tb = traceback.format_exc()
                print(f"[eval] ERROR scoring {eid}:\n{tb}")
                err_rec = {"eid": eid, "error": tb, "ckpt_path": args.ckpt_path}
                out_f.write(json.dumps(err_rec) + "\n")
                out_f.flush()

    # --- aggregate ---
    summary = _summarize(per_traj_records)
    summary["ckpt_path"] = args.ckpt_path
    summary["testset_dir"] = str(testset_dir)
    summary["testset_name"] = manifest.get("name")
    summary["num_inference_steps"] = int(cfg.num_inference_steps)
    summary["num_windows"] = int(args.num_windows)
    summary["start_frame"] = int(args.start_frame)
    summary["timestamp"] = time.time()

    with (output_dir / "aggregate.json").open("w") as f:
        json.dump(summary, f, indent=2)

    print("\n[eval] aggregate:")
    if "overall_lpips" in summary:
        ov = summary["overall_lpips"]
        print(f"  overall  n={ov['n']:3d}  mean={ov['mean']:.4f}  "
              f"std={ov['std']:.4f}  median={ov['median']:.4f}")
    for label, st in (summary.get("by_success") or {}).items():
        if st is not None:
            print(f"  {label:7s}  n={st['n']:3d}  mean={st['mean']:.4f}  "
                  f"std={st['std']:.4f}")
    for s, b in (summary.get("by_noise_scale") or {}).items():
        a = b["all"]
        print(f"  sigma={s:>5}  n={a['n']:3d}  succ_rate={b['success_rate']:.2f}  "
              f"mean_lpips={a['mean']:.4f}")
    print(f"\n[eval] per-traj   -> {per_traj_path}")
    print(f"[eval] aggregate  -> {output_dir / 'aggregate.json'}")
    if args.save_predictions:
        print(f"[eval] predictions -> {pred_root}")


if __name__ == "__main__":
    main()

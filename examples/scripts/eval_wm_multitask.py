#!/usr/bin/env python
"""Evaluate one world-model checkpoint on a multitask test set generated
by ``examples/scripts/collect_wm_multitask_testset.py``.

For each trajectory we run the same autoregressive replay scoring that
the live reward server uses (``examples/reward_model/score_traj.py``)
with ``return_rgb=True``, then compute LPIPS + MSE + PSNR + SSIM on the
decoded prediction/GT RGB frames. Metrics are aggregated overall, by
success vs failure, and by the language instruction the rollout used
(read from ``manifest.json``).

Output:
    <output>/per_traj.jsonl       one record per trajectory
    <output>/aggregate.json       summary + per-bucket breakdowns
    <output>/predictions/<eid>/   gt_vs_pred.mp4  (only with --save-predictions)

Run from the open-world venv on a single GPU::

    cd /scratch/gpfs/AM43/yy4041/open-world
    OPEN_WORLD_ROOT=/scratch/gpfs/AM43/yy4041/open-world \\
    CUDA_VISIBLE_DEVICES=0 \\
    .venv/bin/python /scratch/gpfs/AM43/yy4041/dsrl_pi0/examples/scripts/eval_wm_multitask.py \\
        --testset-dir /scratch/gpfs/AM43/yy4041/wm_testsets/libero_goal_1_multitask_v1 \\
        --ckpt-path /scratch/gpfs/AM43/yy4041/playworld_rollout/0514/8177084_151619_collect_ft_seq_mt/wm_checkpoints/cycle_1/checkpoint-2000.pt \\
        --output /scratch/gpfs/AM43/yy4041/wm_eval/cycle_1_on_libero_goal_1_multitask_v1 \\
        --num-windows 4 --num-inference-steps 25
"""
from __future__ import annotations


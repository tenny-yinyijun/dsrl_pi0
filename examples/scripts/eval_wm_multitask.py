def main():
    parser = argparse.ArgumentParser(
        description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument("--testset-dir", required=True,
                        help="Directory produced by collect_wm_multitask_testset.py "
                             "(also accepts the noise-axis layout from "
                             "collect_wm_testset.py).")
    parser.add_argument("--ckpt-path", required=True,
                        help="World-model checkpoint to evaluate.")
    parser.add_argument("--output", required=True,
                        help="Output directory for per_traj.jsonl + aggregate.json "
                             "(+ optional predictions/).")
    parser.add_argument("--config",
                        default=str(OPEN_WORLD_ROOT / "configs/training/libero_wm.py"))
    parser.add_argument("--dataset-root",
                        default=str(OPEN_WORLD_ROOT / "data/wm_training/libero_processed"),
                        help="Where stat.json lives. Used to normalize "
                             "actions identically to WM training. Defaults "
                             "to the wm_training/libero_processed root the "
                             "0514 collect_ft checkpoints were trained "
                             "against.")
    parser.add_argument("--num-windows", type=int, default=4)
    parser.add_argument("--start-frame", type=int, default=6,
                        help="Fixed start_frame (ignored when "
                             "--random-start-frame is set).")
    parser.add_argument("--skip-his", type=int, default=4)
    parser.add_argument("--num-inference-steps", type=int, default=25)
    parser.add_argument("--device", default="cuda:0")
    parser.add_argument("--save-predictions", action="store_true",
                        help="Save decoded WM predictions vs GT mp4s "
                             "under <output>/predictions/<eid>/.")
    parser.add_argument("--max-trajs", type=int, default=-1,
                        help="Cap eval at this many trajectories "
                             "(-1 = all entries in the manifest).")
    parser.add_argument("--random-start-frame", action="store_true",
                        help="Per-traj, sample start_frame uniformly from "
                             "the in-bounds range "
                             "[num_history*skip_his, T_wm-1-num_windows*(num_frames-1)] "
                             "so all history frames AND all windows stay "
                             "inside the trajectory. If the range is "
                             "empty for a traj, that traj is skipped.")
    parser.add_argument("--seed", type=int, default=0,
                        help="Seed for the per-traj start_frame RNG.")
    args = parser.parse_args()

    testset_dir = Path(args.testset_dir).resolve()
    output_dir = Path(args.output).resolve()
    output_dir.mkdir(parents=True, exist_ok=True)

    print(f"[eval] testset:  {testset_dir}")
    print(f"[eval] ckpt:     {args.ckpt_path}")
    print(f"[eval] output:   {output_dir}")

    manifest = _load_manifest(testset_dir)
    entries = _list_eids(testset_dir, manifest)
    if args.max_trajs > 0:
        entries = entries[: args.max_trajs]
    print(f"[eval] {len(entries)} trajectories to score")

    # ----- model -----
    cfg = _load_libero_args(Path(args.config))
    if not os.path.isabs(cfg.svd_model_path):
        cfg.svd_model_path = str(OPEN_WORLD_ROOT / cfg.svd_model_path)
    if not os.path.isabs(cfg.clip_model_path):
        cfg.clip_model_path = str(OPEN_WORLD_ROOT / cfg.clip_model_path)
    if args.num_inference_steps:
        cfg.num_inference_steps = args.num_inference_steps
    print(f"[eval] cfg num_inference_steps={cfg.num_inference_steps}  "
          f"num_windows={args.num_windows}  "
          f"random_start_frame={args.random_start_frame}  "
          f"fixed_start_frame={args.start_frame}  "
          f"skip_his={args.skip_his}  "
          f"num_history={cfg.num_history}  num_frames={cfg.num_frames}  "
          f"down_sample={cfg.down_sample}")

    # Per-traj cursor advances by (num_frames - 1) per window; need:
    #   start_frame + num_windows * (num_frames - 1) <= T_wm - 1
    # And all history frames must be in-bounds at start:
    #   start_frame >= num_history * skip_his
    min_start = int(cfg.num_history) * int(args.skip_his)
    cursor_span = int(args.num_windows) * (int(cfg.num_frames) - 1)
    # Separate threshold: actions stay in-distribution only while
    #   cursor * down_sample < T_state. When the WM was trained with
    #   T_state == T_latent and frame_len = (T_state-1)//down_sample,
    #   it only ever saw cursor in [0, T_state / down_sample].
    print(f"[eval] start_frame constraints: min={min_start}  "
          f"max=T_wm-1-{cursor_span}")
    rng = np.random.default_rng(int(args.seed))

    device = torch.device(args.device)
    model, pipeline, pipeline_cls = build_model(cfg, args.ckpt_path, device)

    print("[eval] loading LPIPS + SSIM...")
    import lpips as lpips_mod
    lpips_fn = lpips_mod.LPIPS(net="alex", verbose=False).to(device).eval()
    from pytorch_msssim import ssim as ssim_fn  # noqa: F401

    # ----- iterate -----
    per_traj_path = output_dir / "per_traj.jsonl"
    per_traj_records: list[dict] = []
    pred_root = output_dir / "predictions"
    if args.save_predictions:
        pred_root.mkdir(parents=True, exist_ok=True)

    with per_traj_path.open("w") as out_f:
        for k, entry in enumerate(entries):
            eid = entry["eid"]
            t0 = time.perf_counter()
            try:
                split = "train"
                if not (testset_dir / "annotation" / split / f"{eid}.json").exists():
                    split = "val"
                ann = _read_annotation(
                    testset_dir / "annotation" / split / f"{eid}.json")

                latents_per_cam = _load_or_encode_latents(
                    traj_dir=testset_dir, annotation=ann,
                    cfg=cfg, pipeline=pipeline, device=device, eid=eid,
                )
                T_wm = latents_per_cam.shape[0]
                actions = _load_actions(
                    ann, cfg, testset_dir, Path(args.dataset_root), T_wm,
                )
                text = (ann["texts"][0] if ann.get("texts")
                        else ann.get("language_instruction", ""))

                # Per-traj start_frame: random in the bounds-safe range, or
                # the fixed value passed via CLI.
                max_start = T_wm - 1 - cursor_span
                if args.random_start_frame:
                    if max_start < min_start:
                        print(f"[eval] SKIP {eid}: T_wm={T_wm} too short for "
                              f"num_windows={args.num_windows} "
                              f"(need start_frame in [{min_start}, {max_start}])")
                        continue
                    start_frame = int(rng.integers(min_start, max_start + 1))
                else:
                    start_frame = int(args.start_frame)

                # Warn if any window will index actions in the out-of-
                # distribution zone (cursor * down_sample >= T_state).
                T_state = len(ann["observation.state.cartesian_position"])
                action_cursor_max = (T_state - 1) // max(1, int(cfg.down_sample))
                final_cursor = start_frame + cursor_span
                if final_cursor > action_cursor_max:
                    print(f"[eval] WARN {eid}: final cursor={final_cursor} > "
                          f"action_cursor_max={action_cursor_max} "
                          f"(T_state={T_state}, down_sample={cfg.down_sample}). "
                          f"Late windows see action=state[end] (clipped).")

                # return_rgb=True so we can compute MSE/PSNR/SSIM on the
                # same decoded frames LPIPS uses — guaranteed alignment.
                result = score_episode(
                    model=model, pipeline=pipeline, pipeline_cls=pipeline_cls,
                    cfg=cfg,
                    latents_per_cam=latents_per_cam, actions=actions, text=text,
                    start_frame=start_frame,
                    num_windows=args.num_windows,
                    skip_his=args.skip_his,
                    lpips_fn=lpips_fn, device=device,
                    autoregressive=True, verbose=False,
                    return_rgb=True,
                )

                pix = per_frame_pixel_metrics(
                    result["pred_rgb"], result["gt_rgb"],
                    device=device, ssim_fn=ssim_fn,
                )

                elapsed = time.perf_counter() - t0

                rec = {
                    "eid": eid,
                    "instruction": entry.get("instruction",
                                              text if text else ""),
                    "instruction_idx": int(entry.get("instruction_idx", -1)),
                    "replica_idx": int(entry.get("replica_idx", -1)),
                    # Carry noise_scale through when the testset came from
                    # the noise-axis collector (so this eval works on both).
                    "noise_scale": float(entry.get("noise_scale", float("nan"))),
                    "perturbed": bool(entry.get("perturbed", False)),
                    "perturb_mode": str(entry.get("perturb_mode", "none")),
                    "perturb_sigma": float(entry.get("perturb_sigma", 0.0)),
                    "is_success": bool(entry.get("is_success", False)),
                    "env_steps": int(entry.get("env_steps", 0)),
                    "env_seed": int(entry.get("env_seed", -1)),
                    "T_wm": int(T_wm),
                    "start_frame": int(start_frame),
                    "windows_completed": int(result["windows_completed"]),
                    "mean_lpips": float(result["mean_lpips"]),
                    "mean_mse": float(pix["mse"].mean()),
                    "mean_psnr": float(pix["psnr"].mean()),
                    "mean_ssim": float(pix["ssim"].mean()),
                    "per_frame_lpips": [float(x) for x in result["per_frame_lpips"]],
                    "per_frame_mse": [float(x) for x in pix["mse"]],
                    "per_frame_psnr": [float(x) for x in pix["psnr"]],
                    "per_frame_ssim": [float(x) for x in pix["ssim"]],
                    "elapsed_s": elapsed,
                    "ckpt_path": args.ckpt_path,
                }
                if args.save_predictions:
                    try:
                        # Play prediction at the source mp4 rate (fps=20
                        # from collect), NOT cfg.fps (which defaults to 7
                        # in LiberoWMArgs and would make the video play
                        # 7/20 = 2.86x too slow vs. the GT trajectory).
                        ann_fps = int(ann.get("fps") or getattr(cfg, "fps", 10))
                        meta = _save_wm_predictions(
                            output_dir, eid,
                            result["pred_rgb"], result["gt_rgb"],
                            fps=ann_fps,
                        )
                        rec["video_path"] = meta["video_path"]
                    except Exception as e:
                        print(f"[eval] WARN: save predictions for {eid} failed: {e}")

                out_f.write(json.dumps(rec) + "\n")
                out_f.flush()
                per_traj_records.append(rec)

                print(
                    f"[eval] [{k+1:3d}/{len(entries)}] eid={eid}  "
                    f"start={start_frame:3d}  T_wm={T_wm:3d}  "
                    f"succ={rec['is_success']}  "
                    f"lpips={rec['mean_lpips']:.4f}  "
                    f"mse={rec['mean_mse']:.5f}  "
                    f"psnr={rec['mean_psnr']:.2f}  "
                    f"ssim={rec['mean_ssim']:.4f}  "
                    f"windows={rec['windows_completed']}  "
                    f"elapsed={elapsed:.1f}s"
                )
            except Exception:
                tb = traceback.format_exc()
                print(f"[eval] ERROR scoring {eid}:\n{tb}")
                err_rec = {"eid": eid, "error": tb, "ckpt_path": args.ckpt_path}
                out_f.write(json.dumps(err_rec) + "\n")
                out_f.flush()

    # ----- aggregate -----
    summary = _summarize(per_traj_records)
    summary["ckpt_path"] = args.ckpt_path
    summary["testset_dir"] = str(testset_dir)
    summary["testset_name"] = manifest.get("name")
    summary["num_inference_steps"] = int(cfg.num_inference_steps)
    summary["num_windows"] = int(args.num_windows)
    summary["start_frame"] = int(args.start_frame)
    summary["timestamp"] = time.time()

    with (output_dir / "aggregate.json").open("w") as f:
        json.dump(summary, f, indent=2)

    # ----- console report -----
    print("\n[eval] overall:")
    if "overall" in summary:
        for m in _METRIC_KEYS:
            st = summary["overall"].get(m)
            if st is None:
                continue
            print(f"  {m:6s}  n={st['n']:3d}  mean={st['mean']:.4f}  "
                  f"std={st['std']:.4f}  median={st['median']:.4f}")
    for label, st_map in (summary.get("by_success") or {}).items():
        if st_map is None:
            continue
        lp = st_map.get("lpips") or {}
        ms = st_map.get("mse") or {}
        ps = st_map.get("psnr") or {}
        ss = st_map.get("ssim") or {}
        print(f"  {label:9s}  n={lp.get('n', 0):3d}  "
              f"lpips={lp.get('mean', float('nan')):.4f}  "
              f"mse={ms.get('mean', float('nan')):.5f}  "
              f"psnr={ps.get('mean', float('nan')):.2f}  "
              f"ssim={ss.get('mean', float('nan')):.4f}")
    for label, st_map in (summary.get("by_perturb") or {}).items():
        if st_map is None:
            continue
        lp = st_map.get("lpips") or {}
        ms = st_map.get("mse") or {}
        ps = st_map.get("psnr") or {}
        ss = st_map.get("ssim") or {}
        print(f"  {label:9s}  n={lp.get('n', 0):3d}  "
              f"lpips={lp.get('mean', float('nan')):.4f}  "
              f"mse={ms.get('mean', float('nan')):.5f}  "
              f"psnr={ps.get('mean', float('nan')):.2f}  "
              f"ssim={ss.get('mean', float('nan')):.4f}")
    by_instr = summary.get("by_instruction") or {}
    if by_instr:
        print("\n[eval] by instruction:")
        for inst, b in by_instr.items():
            lp = b.get("lpips") or {}
            ms = b.get("mse") or {}
            ps = b.get("psnr") or {}
            ss = b.get("ssim") or {}
            print(f"  n={b['n']:3d}  succ_rate={b['success_rate']:.2f}  "
                  f"lpips={lp.get('mean', float('nan')):.4f}  "
                  f"mse={ms.get('mean', float('nan')):.5f}  "
                  f"psnr={ps.get('mean', float('nan')):.2f}  "
                  f"ssim={ss.get('mean', float('nan')):.4f}  "
                  f"{inst!r}")

    print(f"\n[eval] per-traj   -> {per_traj_path}")
    print(f"[eval] aggregate  -> {output_dir / 'aggregate.json'}")
    if args.save_predictions:
        print(f"[eval] predictions -> {pred_root}")


if __name__ == "__main__":
    main()

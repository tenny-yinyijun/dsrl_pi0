def _load_manifest(testset_dir: Path) -> dict:
    p = testset_dir / "manifest.json"
    if not p.exists():
        raise FileNotFoundError(
            f"{p} not found. Was this test set generated by "
            f"examples/scripts/collect_wm_multitask_testset.py "
            f"or examples/scripts/collect_wm_testset.py?")
    with p.open() as f:
        return json.load(f)


def _list_eids(testset_dir: Path, manifest: dict) -> list[dict]:
    """Return manifest entries that actually have an annotation on disk,
    in stable eid order."""
    out = []
    for entry in manifest.get("entries", []):
        eid = entry["eid"]
        ann = None
        for split in ("train", "val"):
            cand = testset_dir / "annotation" / split / f"{eid}.json"
            if cand.exists():
                ann = cand
                break
        if ann is None:
            print(f"[eval] WARN: no annotation for eid={eid}, skipping")
            continue
        out.append(entry)
    out.sort(key=lambda e: e["eid"])
    return out


def _stats(vals) -> dict | None:
    a = np.asarray(list(vals), dtype=np.float64)
    if a.size == 0:
        return None
    return {
        "n": int(a.size),
        "mean": float(a.mean()),
        "std": float(a.std(ddof=0)),
        "min": float(a.min()),
        "max": float(a.max()),
        "median": float(np.median(a)),
    }


_METRIC_KEYS = ("lpips", "mse", "psnr", "ssim")


def _bucket_summary(records: list[dict]) -> dict:
    """Per-metric stats for the given subset of per-traj records."""
    return {
        m: _stats(r[f"mean_{m}"] for r in records)
        for m in _METRIC_KEYS
    }


def _summarize(per_traj_records: list[dict]) -> dict:
    if not per_traj_records:
        return {"num_trajs": 0}

    overall = _bucket_summary(per_traj_records)

    succ = [r for r in per_traj_records if r["is_success"]]
    fail = [r for r in per_traj_records if not r["is_success"]]
    by_success = {
        "success": _bucket_summary(succ) if succ else None,
        "failure": _bucket_summary(fail) if fail else None,
    }

    pert = [r for r in per_traj_records if r.get("perturbed")]
    clean = [r for r in per_traj_records if not r.get("perturbed")]
    by_perturb = {
        "clean": _bucket_summary(clean) if clean else None,
        "perturbed": _bucket_summary(pert) if pert else None,
    }

    # Bucket by exact instruction string.
    by_instr: dict[str, dict] = {}
    instrs = []
    seen = set()
    for r in per_traj_records:
        inst = r.get("instruction", "")
        if inst not in seen:
            seen.add(inst)
            instrs.append(inst)
    for inst in instrs:
        recs = [r for r in per_traj_records if r.get("instruction", "") == inst]
        s = [r for r in recs if r["is_success"]]
        by_instr[inst] = {
            "n": len(recs),
            "n_success": len(s),
            "success_rate": float(len(s) / max(1, len(recs))),
            **_bucket_summary(recs),
        }

    return {
        "num_trajs": len(per_traj_records),
        "overall": overall,
        "by_success": by_success,
        "by_perturb": by_perturb,
        "by_instruction": by_instr,
    }



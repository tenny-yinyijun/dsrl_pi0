import json
import os
import sys
import time
import traceback
from pathlib import Path

import numpy as np
import torch


OPEN_WORLD_ROOT = Path(
    os.environ.get("OPEN_WORLD_ROOT", "/scratch/gpfs/AM43/yy4041/open-world")
)

# Reuse the helpers the live reward server uses, so eval and live scoring
# can never diverge.
_REWARD_MODEL_DIR = Path(__file__).resolve().parent.parent / "reward_model"
sys.path.insert(0, str(_REWARD_MODEL_DIR))

from score_traj import (  # noqa: E402
    _load_libero_args,
    build_model,
    score_episode,
)
from reward_server import (  # noqa: E402
    _load_actions,
    _load_or_encode_latents,
    _read_annotation,
    _save_wm_predictions,
)


# ---------------------------------------------------------------------------
# Pixel metrics
# ---------------------------------------------------------------------------

def _to_unit_float_tensor(rgb_uint8: np.ndarray, device) -> torch.Tensor:
    """uint8 (T, H, W, 3) -> float32 (T, 3, H, W) in [0, 1] on `device`."""
    t = torch.from_numpy(rgb_uint8).to(device=device, dtype=torch.float32)
    t = t.permute(0, 3, 1, 2) / 255.0
    return t


def per_frame_pixel_metrics(pred_rgb: np.ndarray, gt_rgb: np.ndarray,
                            device, ssim_fn) -> dict[str, np.ndarray]:
    """Compute per-frame MSE, PSNR, SSIM between two equal-shape uint8 RGB
    arrays of shape (T, H, W, 3). Returns three float32 arrays of shape (T,).

    MSE/PSNR are evaluated in [0, 1] floats (so PSNR uses data_range=1).
    SSIM uses pytorch_msssim with data_range=1 and the default 11x11 window.
    """
    T = min(pred_rgb.shape[0], gt_rgb.shape[0])
    pred = _to_unit_float_tensor(pred_rgb[:T], device)
    gt = _to_unit_float_tensor(gt_rgb[:T], device)

    with torch.no_grad():
        sq = (pred - gt) ** 2
        mse = sq.mean(dim=(1, 2, 3))                          # (T,)
        eps = torch.finfo(mse.dtype).tiny
        psnr = -10.0 * torch.log10(mse.clamp(min=eps))        # (T,)

        ssim_vals = torch.empty(T, dtype=torch.float32, device=device)
        for t in range(T):
            # pytorch_msssim.ssim takes (B, C, H, W) with data_range=1.
            ssim_vals[t] = ssim_fn(
                pred[t : t + 1], gt[t : t + 1],
                data_range=1.0, size_average=True,
            )

    return {
        "mse": mse.detach().float().cpu().numpy(),
        "psnr": psnr.detach().float().cpu().numpy(),
        "ssim": ssim_vals.detach().float().cpu().numpy(),
    }


